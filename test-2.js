// Multiplayer snake game built in canvas:

// https://m-qm.github.io/sneaky-snake/

// This game in canvas is built with the help of the following resources:

// https://developer.mozilla.org/en-US/docs/Web/API/Canvas_API/Tutorial/Basic_animations

// https://developer.mozilla.org/en-US/docs/Web/API/Canvas_API/Tutorial/Drawing_shapes

// The players can control their snake with the arrow keys, and letters a, s, d and w.

// The game starts with two randomly positioned snakes and a random food.

// When the player collides with the food, the food is replaced with a new one.

// The previously placed food is converted into a wall.

// If the player collides with the wall, the game is over.

// If the players hit each other, they collide and move in opposite directions.

// If the players eat the food, they earn 1 point.

// The player with the most points wins.
